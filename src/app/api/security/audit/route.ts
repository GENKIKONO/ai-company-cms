import { NextRequest, NextResponse } from 'next/server';
import { runSecurityAudit, generateSecurityReport } from '@/lib/security/audit';

export async function GET(request: NextRequest) {
  try {
    const url = new URL(request.url);
    const format = url.searchParams.get('format') || 'json';
    
    // Admin Ë™çË®º„ÉÅ„Çß„ÉÉ„ÇØÔºàÂü∫Êú¨Ë™çË®ºÔºâ
    const authHeader = request.headers.get('authorization');
    if (!isValidAdminAuth(authHeader)) {
      return NextResponse.json(
        { error: 'Unauthorized access to security audit' },
        { status: 401, headers: { 'WWW-Authenticate': 'Basic realm="Security Audit"' } }
      );
    }

    console.log('üîê Starting security audit...');
    const auditResult = await runSecurityAudit(request);
    
    // „É≠„Ç∞Âá∫Âäõ
    console.log(`üîê Security audit completed: Score ${auditResult.score}/100`);
    if (!auditResult.passed) {
      console.warn('‚ö†Ô∏è Security audit failed - critical issues detected');
    }

    // MarkdownÂΩ¢Âºè„Åß„ÅÆÂá∫Âäõ
    if (format === 'markdown') {
      const markdown = generateSecurityReport(auditResult);
      return new NextResponse(markdown, {
        headers: {
          'Content-Type': 'text/markdown',
          'Content-Disposition': 'attachment; filename="security-audit.md"',
        },
      });
    }

    // JSONÂΩ¢Âºè„Åß„ÅÆÂá∫Âäõ
    return NextResponse.json(auditResult, {
      status: auditResult.passed ? 200 : 206, // 206 = Partial Content (Ë≠¶Âëä„ÅÇ„Çä)
      headers: {
        'Cache-Control': 'no-store, must-revalidate',
      },
    });

  } catch (error) {
    console.error('‚ùå Security audit error:', error);
    
    return NextResponse.json({
      error: 'Security audit failed',
      message: error instanceof Error ? error.message : 'Unknown error',
      timestamp: new Date().toISOString(),
    }, { status: 500 });
  }
}

/**
 * ÁÆ°ÁêÜËÄÖË™çË®º„ÉÅ„Çß„ÉÉ„ÇØ
 */
function isValidAdminAuth(authHeader: string | null): boolean {
  if (!authHeader || !authHeader.startsWith('Basic ')) {
    return false;
  }

  try {
    const credentials = Buffer.from(authHeader.slice(6), 'base64').toString('utf-8');
    const [email, password] = credentials.split(':');
    
    const adminEmail = process.env.ADMIN_EMAIL;
    const adminPassword = process.env.ADMIN_OPS_PASSWORD;
    
    return email === adminEmail && password === adminPassword;
  } catch {
    return false;
  }
}

/**
 * „Çª„Ç≠„É•„É™„ÉÜ„Ç£Áõ£Êüª„ÅÆ„Çπ„Ç±„Ç∏„É•„Éº„É´ÂÆüË°åÔºàPOSTÔºâ
 */
export async function POST(request: NextRequest) {
  try {
    // Admin Ë™çË®º„ÉÅ„Çß„ÉÉ„ÇØ
    const authHeader = request.headers.get('authorization');
    if (!isValidAdminAuth(authHeader)) {
      return NextResponse.json(
        { error: 'Unauthorized' },
        { status: 401 }
      );
    }

    const body = await request.json();
    const { action = 'run' } = body;

    if (action === 'run') {
      // Áõ£ÊüªÂÆüË°å
      const auditResult = await runSecurityAudit(request);
      
      // ÈáçÂ§ß„Å™ÂïèÈ°å„Åå„ÅÇ„ÇãÂ†¥Âêà„ÅØ„Ç¢„É©„Éº„Éà
      if (auditResult.summary.critical > 0) {
        console.error('üö® CRITICAL SECURITY ISSUES DETECTED:');
        auditResult.checks
          .filter(c => c.severity === 'critical' && !c.passed)
          .forEach(check => {
            console.error(`  - ${check.name}: ${check.message}`);
          });
      }

      return NextResponse.json({
        success: true,
        message: 'Security audit completed',
        summary: auditResult.summary,
        score: auditResult.score,
        passed: auditResult.passed,
      });
    }

    return NextResponse.json(
      { error: 'Invalid action' },
      { status: 400 }
    );

  } catch (error) {
    console.error('‚ùå Security audit POST error:', error);
    
    return NextResponse.json({
      error: 'Security audit failed',
      message: error instanceof Error ? error.message : 'Unknown error',
    }, { status: 500 });
  }
}